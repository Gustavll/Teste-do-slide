public class Endereco {
    private String Rua;
    private int Numero;


    public Endereco(){

    }

    public String getRua(){
        return this.Rua;
    }

    public void setRua(String R){
        this.Rua = R;
    }

    public int getNumero(){
        return this.Numero;
    }

    public void setNumero(int num){
        this.Numero = num;
    }

}
